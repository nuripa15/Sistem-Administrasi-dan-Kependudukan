     protected void datatabel(){
           Object[] Baris ={"No Surat Pindah","Nama","Alamat Asal","Alamat Tujuan","tanggal_pindah"};
        model = new DefaultTableModel(null, Baris);
        tabel.setModel(model);
        String sql = "select * from i_data_pindahan";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("no_surat_pindah");
                String b = hasil.getString("nama");
                String c = hasil.getString("alamat_asal");
                String d = hasil.getString("alamat_tujuan");
                String e = hasil.getString("tanggal_pindah");
               
                
                String[] data={a,b,c,d,e,};
                model.addRow(data);
            }
        } catch (Exception e) {
        }
  } 
    
