    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        String sql = "insert into i_data_pindahan values (?,?,?,?,?)";
        try {
            PreparedStatement stat =  conn.prepareStatement(sql);
            stat.setString (1, (String) cb1.getSelectedItem());
            stat.setString (2, t2.getText());
            stat.setString (3, t3.getText());
            stat.setString (4, t5.getText());
            stat.setString (5, (String) box2.getSelectedItem()+'-'+box3.getSelectedItem()+'-'+box4.getSelectedItem());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            t2.requestFocus();
            datatabel();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);   
        }   
        
        
      
    }//GEN-LAST:event_SimpanActionPerformed

